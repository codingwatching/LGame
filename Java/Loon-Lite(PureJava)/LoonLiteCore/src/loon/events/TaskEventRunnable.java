package loon.events;

import loon.utils.HelperUtils;

public class TaskEventRunnable implements TaskRunnable {

	private final EventAction _event;

	public TaskEventRunnable(EventAction e) {
		this._event = e;
	}

	@Override
	public void run() {
		if (_event != null) {
			HelperUtils.callEventAction(_event, this);
		}
	}

}
